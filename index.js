
const recipients = ["Charlie", "Samip", "Ali"]
let birthday = `Thank you, ${recipients}, for the wonderful gift!`
function writeCard(recipients , birthday){
    for( let i =0; i<recipients.length; i++){
        birthday = `Thank you, ${recipients[i]}, for the wonderful gift!`
        return recipients, birthday
    }
}
console.log(writeCard(["Charlie", "Samip", "Ali"], birthday));
